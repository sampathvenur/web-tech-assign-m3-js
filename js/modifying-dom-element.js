function changeText() {
    const textElement = document.getElementById('textContent');
    textElement.textContent = 'New Text Content';
}

function changeStyle() {
    const outputDiv = document.getElementById('output');
    outputDiv.style.backgroundColor = '#ffcc80';
    outputDiv.style.padding = '15px';
    outputDiv.style.border = '2px solid #ff7043';
}
